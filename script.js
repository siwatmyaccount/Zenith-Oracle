function getCardCombinationMessage(cards) {
    if (!cards || cards.length < 2) return null; // ต้องมีไพ่ 2 ใบขึ้นไปถึงจะผสมได้

    const names = cards.map(c => c.name);
    const hasDeath = names.some(n => n.includes("Death"));
    const hasTower = names.some(n => n.includes("Tower"));
    const hasSun = names.some(n => n.includes("Sun"));
    const hasEmpress = names.some(n => n.includes("Empress"));
    const hasDevil = names.some(n => n.includes("Devil"));
    const hasLovers = names.some(n => n.includes("Lovers"));
    const hasPriestess = names.some(n => n.includes("High Priestess"));
    const hasMoon = names.some(n => n.includes("Moon"));
    const hasFool = names.some(n => n.includes("Fool"));
    const hasMagician = names.some(n => n.includes("Magician"));

    // เช็คไพ่หัวกลับทั้งหมด
    const allReversed = cards.every(c => c.isReversed);

    if (hasDeath && hasTower) {
        return "⚠️ <strong>วิกฤตซ้อนวิกฤต (The Tower + Death):</strong> ไพ่เซ็ตนี้เตือนถึงการเปลี่ยนแปลงแบบถอนรากถอนโคน สิ่งที่พังทลายลงมากะทันหันอาจทำให้คุณเจ็บปวด แต่เชื่อเถอะว่าจักรวาลกำลังกวาดล้างสิ่งที่ไม่ใช่ออกไป เพื่อสร้างรากฐานใหม่ที่แข็งแกร่งกว่าเดิมครับ";
    } 
    if (hasSun && hasEmpress) {
        return "✨ <strong>ความอุดมสมบูรณ์ขั้นสุดยอด (The Empress + The Sun):</strong> ยินดีด้วยครับ! นี่คือคอมโบไพ่ที่ดีที่สุดในสำรับ คุณมีเกณฑ์จะได้รับความสุขสมหวังในทุกมิติ สิ่งที่ลงทุนลงแรงไว้จะผลิดอกออกผลอย่างงดงามที่สุดครับ";
    } 
    if (hasLovers && hasDevil) {
        return "🔥 <strong>แรงดึงดูดที่อันตราย (The Lovers + The Devil):</strong> ระวังความลุ่มหลง กิเลส หรือความสัมพันธ์แบบ Toxic ที่ผูกมัดจนทำให้คุณอึดอัด การตัดสินใจของคุณในช่วงนี้มักถูกครอบงำด้วยอารมณ์และตัณหามากกว่าเหตุผลครับ";
    } 
    if (hasPriestess && hasMoon) {
        return "🔮 <strong>เซนส์แรงและความลับ (High Priestess + The Moon):</strong> สัญชาตญาณของคุณกำลังทำงานหนักมาก ไพ่คู่เตือนให้ระวังการโดนหลอกลวง หรือมีสิ่งซ่อนเร้นอยู่เบื้องหลัง จงใช้เซนส์ของคุณจับผิดและอย่าเพิ่งด่วนเชื่ออะไรที่มองเห็นแค่ภายนอกครับ";
    }
    if (hasFool && hasMagician) {
        return "🌟 <strong>ศักยภาพไร้ขีดจำกัด (The Fool + The Magician):</strong> การเริ่มต้นใหม่ครั้งนี้ทรงพลังมาก! คุณมีความกล้าหาญและมีเครื่องมือทุกอย่างครบมือ ขอแค่กล้าที่จะกระโจนลงมือทำ ความสำเร็จแบบปาฏิหาริย์ก็เกิดขึ้นได้ครับ";
    }
    if (allReversed) {
        return "🚧 <strong>พลังงานหยุดชะงัก (ไพ่หัวกลับทั้งหมด):</strong> ไพ่ที่คุณเลือกเป็นหัวกลับทุกใบ บ่งบอกถึงพลังงานที่ถูกบล็อก ช่วงนี้คุณอาจรู้สึกติดขัดไปหมดทุกทาง เหมือนจักรวาลสั่งให้คุณหยุดพัก ทบทวนตัวเอง และอย่าเพิ่งด่วนตัดสินใจทำเรื่องใหญ่ๆ ในช่วงนี้ครับ";
    }

    return null; // ถ้าไม่ตรงกับคอมโบพิเศษเลย ก็ไม่ต้องแสดงอะไร
}

function safePlay(audioObj) {
    if (isMuted) return;
    try {
        if (audioObj.readyState > 0) audioObj.currentTime = 0;
        audioObj.play().catch(e => console.log("Audio play prevented", e));
    } catch(e) {}
}

