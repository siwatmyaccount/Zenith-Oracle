function saveSessionState() {
    if (!user.name) return;
    const activeScene = document.querySelector('.scene.active');
    const sceneId = activeScene ? activeScene.id : 'step-1';
    const state = { user: user, cards: selectedCards, scene: sceneId };
    sessionStorage.setItem('zenith_session', JSON.stringify(state));
}

function clearSessionState() {
    sessionStorage.removeItem('zenith_session');
}

