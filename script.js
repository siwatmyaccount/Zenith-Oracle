// 🌟 1. ระบบกู้คืนเซสชัน (แก้ไขให้รองรับช่องว่าง)
// ==========================================
function restoreSessionState() {
    initDOBSelects(); 
    
    const saved = sessionStorage.getItem('zenith_session');
    if (saved) {
        const state = JSON.parse(saved);
        user = state.user || {};
        
        // 🌟 สำคัญ: เตรียม Array ไพ่ให้มีขนาดจองไว้พอดีกับช่อง (เช่น [ไพ่1, null, ไพ่3])
        let tempCards = state.cards || [];
        if (!tempCards.includes(null) && tempCards.length < (user.spread || 3)) {
            selectedCards = new Array(user.spread || 3).fill(null);
            tempCards.forEach((c, i) => selectedCards[i] = c);
        } else {
            selectedCards = tempCards;
        }

        const currentScene = state.scene || 'step-1';
        // นับจำนวนไพ่ที่ถูกเลือกไปแล้วจริงๆ (ไม่นับช่องว่าง)
        const pickedCount = selectedCards.filter(c => c !== null).length;

        if(document.getElementById('user-name')) document.getElementById('user-name').value = user.name || '';
        
        if(document.getElementById('dob-day') && user.dob) {
            const [yy, mm, dd] = user.dob.split('-');
            if(document.getElementById('dob-year')) document.getElementById('dob-year').value = yy;
            if(document.getElementById('dob-month')) document.getElementById('dob-month').value = mm;
            if(document.getElementById('dob-day')) document.getElementById('dob-day').value = dd;
        }

        if(document.getElementById('user-day')) document.getElementById('user-day').value = user.dayIndex || 0;
        if(document.getElementById('user-topic')) document.getElementById('user-topic').value = user.topic || 'general';
        if(document.getElementById('user-spread')) document.getElementById('user-spread').value = user.spread || 3;
        if(document.getElementById('user-question')) document.getElementById('user-question').value = user.question || '';

        if (currentScene === 'step-2' || (pickedCount > 0 && pickedCount < user.spread)) {
            document.getElementById('step-1').classList.remove('active');
            document.getElementById('step-1').style.display = 'none';
            document.getElementById('step-2').style.display = 'flex';
            setTimeout(() => document.getElementById('step-2').classList.add('active'), 50);

            generateDeck();
            
            setTimeout(() => {
                selectedCards.forEach(c => {
                    if (c !== null && c.deckElementIndex !== undefined) {
                        const deckCard = document.querySelector(`.card-mini[data-z-index="${c.deckElementIndex}"]`);
                        if(deckCard) deckCard.classList.add('picked');
                    }
                });
            }, 100);

            updateSlotsDisplay();
            
        } else if (currentScene === 'step-3' || pickedCount === user.spread) {
            showResults('step-1');
        }
    } else {
        initDOBSelects();
    }
}
