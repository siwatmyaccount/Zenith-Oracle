function initDOBSelects() {
    const d = document.getElementById('dob-day');
    const m = document.getElementById('dob-month');
    const y = document.getElementById('dob-year');
    if(!d || !m || !y) return;
    
    for(let i=1; i<=31; i++) {
        d.innerHTML += `<option value="${i.toString().padStart(2, '0')}">${i}</option>`;
    }
    const months = ["มกราคม","กุมภาพันธ์","มีนาคม","เมษายน","พฤษภาคม","มิถุนายน","กรกฎาคม","สิงหาคม","กันยายน","ตุลาคม","พฤศจิกายน","ธันวาคม"];
    months.forEach((name, i) => {
        m.innerHTML += `<option value="${(i+1).toString().padStart(2, '0')}">${name}</option>`;
    });
    const currentYear = new Date().getFullYear();
    for(let i = currentYear; i >= 1940; i--) {
        y.innerHTML += `<option value="${i}">${i + 543}</option>`;
    }
}

